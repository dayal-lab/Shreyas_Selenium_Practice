package Java_basics;

public class Array_Replace_index_position {

	public static void main(String[] args) 
	{
		String [] cars = {"Volvo","Toyota","Honda"};
		
		//at cars[2] is "Honda", then replace with "Hyundai" and print
		cars[2]= "Hyundai"; 
		
		System.out.println("Car at array location 2 is replaced with	:"+cars[2]);

	}

}

