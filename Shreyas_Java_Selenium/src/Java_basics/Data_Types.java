package Java_basics;

public class Data_Types 
{
	public static void main(String[] args) 
	
	{
		char c 		= 'S';
		String s 	= "Shreyas";
		int i 		= 180;
		float f 	= 1.22f;
		double d 	= 12.55d;
		long l 		= 3456767L;
		Boolean b 	= true ;
		
		System.out.println("First letter		: "	+c);
		System.out.println("Full Name is		: "	+s);
		System.out.println("No. of days		: "	+i);
		System.out.println("Float values		: "	+f);
		System.out.println("Double values		: "	+d);
		System.out.println("Long value		: "	+l);
		System.out.println("Boolean condition	: "	+b);
		
	}

}
